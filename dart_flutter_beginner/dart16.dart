Operatörlere Giriş
Bir sonraki derste, Dart tarafından sağlanan operatörlere genel bir bakış 
verilecektir.

Operatörler, verileri değiştirmek veya işlemek için kullanılan işlemleri 
gerçekleştiren sembollerdir . Verileri manipüle etmek, herhangi bir programlama 
dilinin önemli bir parçasıdır ve Dart, temel türleri için zengin bir operatör 
seti sağlayarak farklı değildir.

Operatör Türleri
