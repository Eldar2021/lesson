Operators and Expressions#
Operatörler ve İfadeler

Bir İfade , bir değeri değerlendiren özel bir ifade türüdür. Örneğin 
matematikte 1 + 1 ifadesi 2 olarak değerlendirildiği için bir ifadedir .

İfadeler iki şeyden oluşur; işlenenler ve operatörler .

Operatörler genellikle bir infix notasyonu izler. İnfix notasyonu , 
operatörün iki işlenen arasında bulunduğu yerdir.

İşlenenler , operatörün üzerinde bir işlem gerçekleştirdiği veri nesneleridir. 
Başka bir deyişle, operatörler, bir değer üretmek için işlenenlerin nasıl 
işleneceğini tanımlar.

Farklı operatörler farklı işlemler gerçekleştirir; bu nedenle, verilerinizin 
nasıl işlenmesini istediğinizi ve doğru olanı seçebilmek için her operatörün 
ne yaptığını bilmeniz gerekir. Bu bölümün amacı budur.

Şimdi bir sonraki derste aritmetik operatörlerle başlayarak farklı operatör 
türlerine geçelim.
*/
