Giriş #
Kontrol yapıları , ifadelerin (kod satırlarının) yürütüldüğü akışı belirleyen 
veya dikte eden kod blokları olarak düşünülebilir; bunlar temelde kod akışını 
kontrol eden ifadelerdir ( kontrol akışı ifadeleri ). Örneğin, bir kod bloğu 
sıralı bir şekilde akabilir; burada bir kontrol yapısı, ifadelerinin sıralı 
olarak yürütülmesini sağlar. Bir kod bloğu , ifadelerinin yinelemeli olarak 
yürütülmesini sağlayan kontrol yapılarının bulunduğu yinelemeli bir şekilde 
de akabilir .

Dart'ta Kontrol Akış İfadeleri #
Dart, Dart kodunuzun akışını kontrol etmek için kullanılabilecek birden 
çok ifade sağlar.

Bu bölümde aşağıdaki ifadeleri tartışacağız:

if ve else

for döngüler

whileve do-while döngüler

break ve continue

switch vecase

assert
