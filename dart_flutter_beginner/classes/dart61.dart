Giriş #
Artık nesnelere ve sınıflara aşina olduğunuza göre, nesne yönelimli 
programlamanın temel kavramlarından biri olan kalıtım hakkında konuşalım.

Kalıtım, zaten var olan sınıftan yeni bir sınıf oluşturabileceğiniz bir 
kavramdır. Yeni sınıf , mevcut sınıfın özelliklerini (örnek değişkenleri) 
ve yöntemlerini devralır . Sınıf üyelerini miras alan sınıf, alt sınıf olarak 
bilinirken, kendisinden miras alınan sınıf, üst sınıf olarak bilinir .

Her sınıf, Objectsınıf hiyerarşisinin en tepesinde yer alan ve kendi üst 
sınıfı olmayan üst sınıfın bir alt sınıfıdır.

Bir İlişkidir # _
Aklınıza gelebilecek bir sonraki soru, kalıtımı ne zaman kullanıyoruz? Cevap, 
nesneler arasında bir IS A ilişkisiyle karşılaştığımız her yerde , 
kalıtımı kullanabiliriz.
