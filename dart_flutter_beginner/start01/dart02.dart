Dart: Bir Tarih #
Dart 1.0, 14 Kasım 2013'te Google tarafından piyasaya sürüldü ve Lars Bak ve 
Kasper Lund tarafından kuruldu. Geliştiricilerin modern web ve mobil 
uygulamalar oluşturmasına yardımcı olmayı amaçlar. Flutter ile istemciyi, 
sunucuyu ve şimdi mobili kapsar. Sanal makine, çekirdek kitaplıklar ve paket 
yönetimi deposu gibi bir dizi araçla birlikte gelir ve bir sonraki projenize 
başlamanız için yeterli mühimmatı sağlar.

Dart Nesne Yönelimli #
Smalltalk 1970'lerde piyasaya sürüldü ve ilk saf nesne yönelimli programlama 
dillerinden biriydi. Artık nesne yönelimli programlamayı her yerde 
bulabilirsiniz. Programlama dilleri dünyasına hakim oldu.

Nesne yönelimli programlamanın ardındaki kavram oldukça basittir: en önemsiz 
programlar dışında hepsi belirli bir yapı biçimi gerektirir.

Bu özel yapıya ulaşmanın en net yolu, saklama kapları konseptini kullanmaktır. 
Bir programlama dili, veriler ve veriler üzerinde gerçekleştirilecek işlemler 
olarak ikiye ayrılabilir. Belirli verileri ve işlemleri bir tür kapsayıcıda 
saklayabiliriz. Ayrıca, bu kaplar genel olarak yapılmıştır. Bu nedenle, 
yalnızca verileri ve işlemleri depolamakla kalmazlar, kendileri de diğer 
kaplarda saklanabilen ve diğer işlemlere parametre olarak aktarılabilen 
değerlerdir. Nesne yönelimli programlamada bu kaplar nesneler olarak bilinir.

Smalltalk'ın mucidi Alan Kay, bu şekilde en basit nesnenin tam bir bilgisayarla 
aynı yapı ilkesine sahip olduğunu belirtti: verileri, resmi bir arayüz altında 
işlemlerle birleştirir.

Ve şimdi, nesne yönelimli programlama çok sayıda dilde bulunabilse de, çok azı 
Smalltalk tarafından belirlenen ilkeleri takip ediyor. Dart ise her değeri bir 
nesne olan saf nesne yönelimli bir programlama dilidir.

Dart: JavaScript'in Gözünden #
Dart, ağırlıklı olarak temel aldığı programlama dili olan JavaScript'ten daha 
fazla yapıya sahip, temiz, basit, sınıf tabanlı nesne yönelimli bir dildir. 
Programlama dillerinde kolayca yeniden düzenleme yapabilmeleri ve büyük web 
uygulamaları oluşturabilmeleri için bir yapıya sahip olmakla ilgilenen 
